<template>
 <div class="toast" v-show="isShow">
 {{message}}
 </div>
</template>

<script>
export default {
 data() {
  return {
isShow:false,
message:''
  }
 },
 methods:{
     show(message,duration=1500){
         this.isShow=true
         this.message=message
          setTimeout(()=>{
             this.isShow=false
             this.message=''
          },2000)
     }
 }
}
</script>

<style scoped>
.toast{
    position: fixed;
    z-index: 999;
    top:50%;
    left: 50%;
    transform: translate(-50% , -50%);
    background-color: rgba(0,0,0,.75);
    color: #fff;
    padding: 8px;
}
</style>
